SELECT
    m.MAKE,
    m.TOTAL_VEHICLES,
    m.MARKET_SHARE_PCT,
    m.BEV_COUNT,
    m.PHEV_COUNT,
    m.AVG_ELECTRIC_RANGE,
    SNOWFLAKE.CORTEX.COMPLETE(
        'mistral-large',
        CONCAT(
            'You are a senior automotive industry analyst. Generate a concise executive summary for the EV manufacturer: ', m.MAKE, '. ',
            'Use the following data points to provide insights on market positioning, growth potential, and competitive analysis. ',
            'Data: ',
            'Total Vehicles Registered: ', m.TOTAL_VEHICLES::STRING, '. ' ,
            'Market Share: ', m.MARKET_SHARE_PCT::STRING, '%. ',
            'BEV (Battery Electric) Count: ', m.BEV_COUNT::STRING, '. ' ,
            'PHEV (Plug-in Hybrid) Count: ', m.PHEV_COUNT::STRING, '. ' ,
            'Average Electric Range: ', m.AVG_ELECTRIC_RANGE::STRING, ' miles. ',
            'Total Models Offered: ', m.TOTAL_MODELS::STRING, '. ',
            'Years in Market: ', m.YEARS_IN_MARKET::STRING, ' (', m.EARLIEST_MODEL_YEAR::STRING, ' - ', m.LATEST_MODEL_YEAR::STRING, '). ',
            'Cover these sections in 3-4 sentences each: ',
            '1) Market Positioning - current standing and brand strength. ',
            '2) Growth Insights - trajectory and electrification strategy (BEV vs PHEV mix). ',
            '3) Competitive Analysis - strengths, weaknesses, and how they compare to the broader EV market.'
        )
    ) AS AI_EXECUTIVE_SUMMARY,
    CURRENT_TIMESTAMP() AS GENERATED_AT
FROM EV_ANALYTICS_DB.GOLD.EV_ADOPTION_BY_MANUFACTURER m
ORDER BY m.TOTAL_VEHICLES DESC;